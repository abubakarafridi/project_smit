// Commenting in JavaScript:
//     Improves code readability and maintainability.
//     Useful for explaining code logic, adding notes, and testing/debugging.
// JavaScript commenting methods:
//     Single-line comments: Start with `//`, everything from there to the end of the line is ignored.
//     Multi-line comments (block comments): Start with `/*`, end with `*/`, can span multiple lines.
// Best practices:
//     Use spaces around comment delimiters (`//` and `/*`).
//     Use block comments for longer explanations.